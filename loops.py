#for loop

for i in range(5):
    print(i)

for j in range(2, 10, 2):
    print(j)
    

#while loop
c = 5
while c > 0:
    print(c)
    c -= 1

#while loop with else

n = 4
while n > 0:
    print(n)
    n -= 1
else:  # executes when the loop condition becomes false and loop is not terminated by break
    print("loop is ended")


